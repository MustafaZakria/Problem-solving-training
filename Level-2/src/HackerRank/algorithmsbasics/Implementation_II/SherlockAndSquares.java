class Result9 {

    /*
     * Complete the 'squares' function below.
     *
     * The function is expected to return an INTEGER.
     * The function accepts following parameters:
     *  1. INTEGER a
     *  2. INTEGER b
     */

    public static int squares(int a, int b) {
        int result = 0,
                i = 1;

        while ((a / i) > i) {
            i++;
        }

        for (int j = i; Math.pow(j, 2) <= b; j++) {
            if(Math.pow(j, 2) >= a)
                result++;
        }
        return result;
    }

}

